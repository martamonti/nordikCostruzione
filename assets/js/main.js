$(function () {
  $(document).scroll(function () {
    var $nav = $(".fixed-top");
    $nav.toggleClass('scrolled', $(this).scrollTop() > $nav.height());
  });
});

$(function () {
  $(document).scroll(function () {
    var $nav = $(".2nav");
    $nav.toggleClass('scrolled2', $(this).scrollTop() > $nav.height());
  });
});

